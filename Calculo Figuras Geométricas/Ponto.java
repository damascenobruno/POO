public abstract class Ponto{
    private double x;
    private double y;

    public double getX() {
        return this.x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return this.y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public abstract String toString();

    public abstract void solicitaDados();


}